def extract_plate_number(text: str) -> str:
    patterns = [
        r'([京津沪渝冀豫云辽黑湘皖鲁新苏浙赣鄂桂甘晋蒙陕吉闽贵粤青藏川宁琼使领][A-Z][A-Z0-9]{5,6})',
        r'([京津沪渝冀豫云辽黑湘皖鲁新苏浙赣鄂桂甘晋蒙陕吉闽贵粤青藏川宁琼使领][A-Z]\s?[A-Z0-9]{5,6})',
        r'(车牌\s*[:：]?\s*([京津沪渝冀豫云辽黑湘皖鲁新苏浙赣鄂桂甘晋蒙陕吉闽贵粤青藏川宁琼使领][A-Z][A-Z0-9]{5,6}))'
    ]
    
    for pattern in patterns:
        match = re.search(pattern, text)
        if match:
            result = match.group(1).strip()
            return result.replace(' ', '')
    
    return ''

def extract_phone_number(text: str) -> str:
    patterns = [
        r'(?:\+?86)?1[3-9]\d{9}',
        r'(?:\+?86)?\s*1[3-9]\d{2}\s*\d{4}\s*\d{4}',
        r'(电话|手机|联系电话)\s*[:：]?\s*(\d{11})',
        r'(\d{11})'
    ]
    
    for pattern in patterns:
        match = re.search(pattern, text)
        if match:
            if match.lastindex and match.lastindex > 0:
                result = match.group(match.lastindex)
            else:
                result = match.group()
            result = ''.join(filter(str.isdigit, result))
            if len(result) == 11 or (len(result) == 13 and result.startswith('86')):
                if len(result) == 13 and result.startswith('86'):
                    result = result[2:]
                return result
    
    return ''

